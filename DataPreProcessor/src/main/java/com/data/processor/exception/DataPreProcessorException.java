package com.data.processor.exception;

public class DataPreProcessorException extends Exception {


    private static final long serialVersionUID = 1L;

    public DataPreProcessorException() {
        super();
    }


    public DataPreProcessorException(Throwable cause) {
        super(cause);
    }

    public DataPreProcessorException(String message) {
        super(message);
    }


    public DataPreProcessorException(String message, Throwable cause) {
        super(message, cause);
    }

}
